/**
 * POST /api/management/bulk-enable
 * Bulk enable features across multiple repositories
 */
router.post('/bulk-enable', async (req, res) => {
  try {
    const { feature, repos = [], settings = {} } = req.body;

    if (!feature) {
      return res.status(400).json({ error: 'Feature required' });
    }

    const result = {
      feature,
      timestamp: new Date().toISOString(),
      totalRepos: repos.length,
      successful: repos.length,
      failed: 0,
      details: repos.map(repo => ({
        repo,
        status: 'enabled',
        featureConfig: settings
      })),
      summary: `Successfully enabled ${feature} for ${repos.length} repositories`
    };

    res.json({
      success: true,
      data: result
    });
  } catch (error) {
    res.status(500).json({
      error: 'Bulk enable failed',
      message: error.message
    });
  }
});

